def test_create_solution_success(solutions_test_client):
    """Test successful solution creation"""
    client, user, problem = solutions_test_client
    
    # Authenticate user
    with client.session_transaction() as sess:
        sess['_user_id'] = str(user.id)
        sess['_fresh'] = True
    
    solution_data = {
        'problem_id': problem.id,
        'name': 'Test AI Generated Solution',
        'description': 'This is a comprehensive solution generated by AI to address the identified problem with specific implementation steps.'
    }
    
    response = client.post('/solutions/new', 
                          json=solution_data)
    
    assert response.status_code == 200
    data = response.get_json()
    
    assert data['success'] is True
    assert 'solution_id' in data
    assert 'message' in data
    
    # Verify solution was created in database
    solution = Solution.query.filter_by(name='Test AI Generated Solution').first()
    assert solution is not None
    assert solution.problem_id == problem.id
    assert solution.created_by == user.id

