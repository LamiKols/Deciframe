def test_generate_intelligent_solutions():
    """Test intelligent solution generation"""
    from ai.routes import generate_intelligent_solutions
    from models import Problem, PriorityEnum
    
    # Create test problem with performance issue
    problem = Problem(
        title='System Performance Issues',
        description='The application is very slow during peak hours and users are experiencing timeouts',
        priority=PriorityEnum.High
    )
    
    solutions = generate_intelligent_solutions(problem)
    
    assert len(solutions) == 3
    assert all('name' in s and 'description' in s for s in solutions)
    assert 'Performance' in solutions[0]['name']
    assert 'Infrastructure' in solutions[1]['name'] or 'Optimization' in solutions[1]['name']